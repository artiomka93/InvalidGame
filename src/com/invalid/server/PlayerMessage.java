package com.invalid.server;

public class PlayerMessage {

	private String playerId;
	private String x;
	private String y;
	private String direction;
	
	public String getPlayerId() {
		return playerId;
	}
	
	public String getX() {
		return x;
	}
	
	public String getY() {
		return y;
	}
	
	public String getDirection() {
		return direction;
	}
	
	
}

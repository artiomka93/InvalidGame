package com.invalid.server;

public class BulletMessage {
	
	private String bulletId;
	private String x;
	private String y;
	private String direction;
	
	public String getBulletId() {
		return bulletId;
	}
	
	public String getX() {
		return x;
	}
	
	public String getY() {
		return y;
	}
	
	public String getDirection() {
		return direction;
	}

}
